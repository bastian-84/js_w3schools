//let sOutput01 = fnGetEl('output01');
let sOutput01 = fnGetEl('#output01');

// ====================================================== //
// ================== setInterval( ) ================== //
// ====================================================== //

let fnShowTime = () => {
    let sDate = new Date();
    sOutput01.innerHTML = sDate.getHours() + ":" + sDate.getMinutes() + ":" + sDate.getSeconds();
}

setInterval(fnShowTime, 1000);

