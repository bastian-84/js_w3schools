// The for/in statement loops through the properties of an object.

let sTxt = '';
let aPstls = { 
    name: 'M9', 
    cal: '9 mm', 
    action: 'SA'
};

let i = 0;
for (i in aPstls){
    sTxt += aPstls[i] + ' ';
}

console.log(sTxt);

